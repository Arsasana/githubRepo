package com.ks.riskcontrol.pojos;

public class tempObject {
    private int pNode;
    private String type;

    public tempObject(int pNode, String type) {
        this.pNode = pNode;
        this.type = type;
    }

    public int getpNode() {
        return pNode;
    }

    public void setpNode(int pNode) {
        this.pNode = pNode;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
