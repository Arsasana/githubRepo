package com.ks.riskcontrol.pojos;

public class NextVal {
    Integer nextVal;

    public Integer getNextVal() {
        return nextVal;
    }

    public void setNextVal(Integer nextVal) {
        this.nextVal = nextVal;
    }

    @Override
    public String toString() {
        return "NextVal{" +
                "nextVal=" + nextVal +
                '}';
    }
}
